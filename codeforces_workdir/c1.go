package main

import (
	"fmt"
)


func main() {
	// export PATH=$PATH:/usr/local/go/bin
	// go run c.go < input.txt

	var L, R int64
	fmt.Scanln(&L, &R)

	var answer int = 0
	for a := L; a <= R; a++ {
		for b := L; b <= a; b++ {
			if ((a + b) | (a - b)) == a + b {
				//fmt.Println(a, b)
				answer++
			}
		}
	}
	fmt.Println(answer)
}
